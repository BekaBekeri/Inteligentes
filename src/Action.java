
public class Action {

	private int newX;
	private int newY;
	private int sandN;
	private int sandS;
	private int sandW;
	private int sandE;
	
	public Action(){
		
	}
	public int getNewX() {
		return newX;
	}
	
	public int getNewY() {
		return newY;
	}
	public void setNewY(int newY) {
		this.newY = newY;
	}
	public int getSandN() {
		return sandN;
	}
	public void setSandN(int sandN) {
		this.sandN = sandN;
	}
	public int getSandS() {
		return sandS;
	}
	public void setSandS(int sandS) {
		this.sandS = sandS;
	}
	public int getSandW() {
		return sandW;
	}
	public void setSandW(int sandW) {
		this.sandW = sandW;
	}
	public int getSandE() {
		return sandE;
	}
	public void setSandE(int sandE) {
		this.sandE = sandE;
	}
	
}
